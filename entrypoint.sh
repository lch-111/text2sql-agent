#!/bin/sh
# =============================================================================
# Docker 容器入口脚本 — FastAPI 服务
# 功能：初始化数据库 → 构建向量索引 → 启动应用
# =============================================================================

set -e

echo "========================================"
echo "  Redis 已就绪"
echo "========================================"

# --- 运行评估（失败不阻塞启动，超时30秒）---
echo "[入口] 正在生成评估报告..."
timeout 30 python evaluator.py 2>/dev/null || echo "[入口] 评估跳过（首次启动或数据库未连接）"

# --- 启动应用 ---
echo "[入口] 跳过数据初始化（由用户自行连接数据库或上传文件）"

echo "========================================"
echo "  启动 FastAPI 服务..."
echo "========================================"

exec uvicorn app:app \
    --host 0.0.0.0 \
    --port 8000 \
    --workers 1 \
    --log-level info
